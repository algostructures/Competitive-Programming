	static class Solver implements Runnable
	{
		int t;
		String s;
		BufferedReader in;
		OutputWriter out;

		public Solver(BufferedReader in, OutputWriter out)
		{
			this.in = in;
			this.out = out;
		}

		@Override
		public void run()
		{
			try
			{
				t = Integer.parseInt(in.readLine());

				for (int i = 0; i < t; i++)
				{
					s = in.readLine();

					out.println("Case #" + (i + 1) + ": " + count(s));
				}
			}
			catch (IOException e)
			{
				e.printStackTrace();
			}
		}

		public int count(String s)
		{
			char[] pancakes = s.toCharArray();
			int flips = 0;
			int len = s.length();

			while (true)
			{
				int left, right;

				left = 0;
				right = len - 1;

				while (right >= 0 && pancakes[right] == '+')
					right--;

				if (right == -1)
					return flips;

				boolean entered = false;

				while (left < right && pancakes[left] == '+')
				{
					pancakes[left++] = '-';
					entered = true;
				}

				if (left == len)
					return flips;

				if (entered)
					flips++;

				for (int i = 0; i <= right; i++)
				{
					if (pancakes[i] == '-')
						pancakes[i] = '+';
					else
						pancakes[i] = '-';
				}

				flips++;
			}
		}

	}

