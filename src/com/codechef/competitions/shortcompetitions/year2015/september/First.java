			k = Integer.parseInt(line[1]);

			forg = bReader.readLine().split(" ");
			
			String mod[] = new String[100000];
			int count = 0;
			
			for (int m = 0; m < k; m++)
			{
				line2 = bReader.readLine().split(" ");
				
				count = append(mod, count, line2);
			}
			
